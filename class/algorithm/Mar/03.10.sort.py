# 1. sorted + lambda 함수를 사용하여 아래와 같이 정렬해서 출력해 주세요 (알파벳순서대로 출력)

arr=['A','C','B','F','BB','G','DD','E','B','AA']

# 출력결과: ['A', 'AA', 'B', 'B', 'BB', 'C', 'DD', 'E', 'F', 'G']
